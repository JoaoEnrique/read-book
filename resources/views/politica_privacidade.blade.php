    <h1>Política de Privacidade</h1>

    <h2>1. Introdução</h2>
    <p>Bem-vindo ao nosso aplicativo de leitura de livros. Esta Política de Privacidade explica como coletamos, usamos, divulgamos e protegemos as informações dos nossos usuários. Ao utilizar nosso aplicativo, você concorda com as práticas descritas nesta política.</p>

    <h2>2. Informações que Coletamos</h2>
    <p><strong>Informações Pessoais:</strong></p>
    <ul>
        <li>Nome</li>
        <li>Endereço de e-mail</li>
        <li>Senha</li>
    </ul>
    <p><strong>Informações de Uso:</strong></p>
    <ul>
        <li>Livros cadastrados</li>
        <li>Data de leitura</li>
        <li>Status de leitura</li>
    </ul>

    <h2>3. Uso das Informações</h2>
    <p>Utilizamos as informações coletadas para:</p>
    <ul>
        <li>Prover e melhorar nossos serviços</li>
        <li>Personalizar a experiência do usuário</li>
        <li>Facilitar a gestão dos livros lidos</li>
        <li>Enviar notificações e atualizações relevantes</li>
    </ul>

    <h2>4. Compartilhamento de Informações</h2>
    <p>Não compartilhamos suas informações pessoais com terceiros, exceto:</p>
    <ul>
        <li>Quando necessário para cumprir obrigações legais</li>
        <li>Para proteger nossos direitos e propriedade</li>
        <li>Com provedores de serviço que nos auxiliam na operação do aplicativo, desde que eles sigam práticas de privacidade adequadas</li>
    </ul>

    <h2>5. Segurança das Informações</h2>
    <p>Implementamos medidas de segurança técnicas e organizacionais para proteger suas informações contra acessos não autorizados, perda, uso indevido ou divulgação.</p>

    <h2>6. Acesso e Controle das Informações</h2>
    <p>Você tem o direito de:</p>
    <ul>
        <li>Acessar suas informações pessoais</li>
        <li>Corrigir ou atualizar informações incorretas</li>
        <li>Solicitar a exclusão de suas informações pessoais</li>
        <li>Limitar ou opor-se ao processamento de suas informações pessoais</li>
    </ul>
    <p>Para exercer esses direitos, entre em contato conosco através do e-mail fornecido no aplicativo.</p>

    <h2>7. Retenção de Dados</h2>
    <p>Retemos suas informações pessoais enquanto sua conta estiver ativa ou conforme necessário para fornecer nossos serviços. Podemos reter e usar suas informações conforme necessário para cumprir obrigações legais, resolver disputas e fazer cumprir nossos acordos.</p>

    <h2>8. Alterações na Política de Privacidade</h2>
    <p>Podemos atualizar esta Política de Privacidade periodicamente. Notificaremos você sobre quaisquer mudanças publicando a nova política no aplicativo. Recomendamos revisar esta política regularmente para se manter informado sobre nossas práticas de privacidade.</p>

    <h2>9. Contato</h2>
    <p>Se você tiver alguma dúvida ou preocupação sobre esta Política de Privacidade, entre em contato conosco através do e-mail: readbook2059@gmail.com.</p>

    <h2>10. Consentimento</h2>
    <p>Ao utilizar nosso aplicativo, você consente com a coleta e uso de suas informações conforme descrito nesta Política de Privacidade.</p>

